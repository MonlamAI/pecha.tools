export default function getRandomLightColor(index: number) {
  const lightColors = [
    "bg-yellow-300",
    "bg-blue-300",
    "bg-green-300",
    "bg-pink-300",
    "bg-purple-300",
    "bg-indigo-300",
    "bg-red-300",
    "bg-amber-300",
    "bg-lime-300",
    "bg-cyan-300",
    "bg-sky-300",
    "bg-violet-300",
  ];
  return lightColors[index];
}
